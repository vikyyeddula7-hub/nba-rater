def get_all_teams():
    from app import TEAM_COLORS
    teams = []
    for team_name, team_ovr in TEAM_OVERALLS.items():
        roster = TEAM_ROSTERS.get(team_name, [])
        roster_sorted = sorted(roster, key=lambda p: p["ovr"], reverse=True)
        color = TEAM_COLORS.get(team_name, "#e8523a")
        if team_ovr >= 84:   tier = {"label": "Contender",   "badge": "diamond"}
        elif team_ovr >= 82: tier = {"label": "Playoff Team", "badge": "gold"}
        elif team_ovr >= 80: tier = {"label": "Bubble Team",  "badge": "silver"}
        else:                tier = {"label": "Rebuilding",   "badge": "grey"}
        teams.append({
            "name":     team_name,
            "ovr":      team_ovr,
            "roster":   roster_sorted,
            "color":    color,
            "tier":     tier,
            "star":     roster_sorted[0]["name"] if roster_sorted else "",
            "star_ovr": roster_sorted[0]["ovr"]  if roster_sorted else 0,
            "star_id":  roster_sorted[0]["id"]   if roster_sorted else 0,
        })
    teams.sort(key=lambda t: t["ovr"], reverse=True)
    return teams
